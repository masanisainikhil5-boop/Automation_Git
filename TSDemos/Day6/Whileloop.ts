//While loop - A while loop executes as long as the condition is true.
let i = 1
let result = " "
while(i<=10)
    {
    result = result+i+" "
    i++
}
console.log(result)

//Even numbers between 1 to 20
let x = 2
let ans = " "
while(x<=20){
    ans = ans+x+" "
    x=x+2
}
console.log(ans)

//Print numbers 10 9 8 ...
let y=10
let prin = " "

while(y>0){
    prin = prin+y+" "
    y--
}
console.log(prin)